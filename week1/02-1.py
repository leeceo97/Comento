# 02-1 숫자형

# 정수형
a = 123
a = -178
a = 0

# 실수형
a = 1.2
a = -3.45

# 8진수표현은 숫자가 0o 또는 0O로 시작하면 된다.
a = 0o177

# 16진수를 만들기 위해서는 0x로 시작하면 된다.
a = 0x8ff
b  = 0xABC

# 숫자형을 활용하기 위한 연산자
# 사칙연산

# x의 y제곱을 나타내는 **연산자
a = 3
b = 4
a ** b
# 결과값 3의 4제곱 81

# 나눗셈 후 나머지를 반환하는 % 연산자
7 % 3
# 결과값 1

# 나눗셈 후 몫을 반환하는 /, // 연산자
7 / 4 
# 결과값 1.75
7 // 4
# 결과값 1 --> 정수만 반환




